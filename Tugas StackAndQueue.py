class Stack:
    def __init__(self):
        self.items = []
    def is_empty(self):
        return len(self.items) == 0
    def push(self, items):
        self.items.append(items)
    def pop(self):
        if self.is_empty():
            raise Exception("Stack is empty")
        return self.items.pop()
